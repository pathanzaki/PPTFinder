SLIDE_W = Inches(13.33)
SLIDE_H = Inches(7.5)

C_BG_DARK   = RGBColor(0x0A, 0x0A, 0x18)
C_BG_MID    = RGBColor(0x10, 0x10, 0x28)
C_BG_LIGHT  = RGBColor(0xF0, 0xF2, 0xFF)
C_PANEL     = RGBColor(0x14, 0x14, 0x30)
C_ACCENT1   = RGBColor(0x7C, 0x3A, 0xFF)
C_ACCENT2   = RGBColor(0x00, 0xD4, 0xFF)
C_ACCENT3   = RGBColor(0xFF, 0x6B, 0x35)
C_WHITE     = RGBColor(0xFF, 0xFF, 0xFF)
C_TEXT_DARK = RGBColor(0x12, 0x12, 0x2A)
C_MUTED     = RGBColor(0x88, 0x88, 0xAA)

def rgb(r, g, b): return RGBColor(r, g, b)

def add_rect(slide, x, y, w, h, fill_color, line_color=None):
    shape = slide.shapes.add_shape(1, Inches(x), Inches(y), Inches(w), Inches(h))
    shape.fill.solid()
    shape.fill.fore_color.rgb = fill_color
    if line_color:
        shape.line.color.rgb = line_color
        shape.line.width = Pt(1)
    else:
        shape.line.fill.background()
    return shape

def add_textbox(slide, x, y, w, h, text, font_size, bold=False,
                color=C_WHITE, align=PP_ALIGN.LEFT, italic=False, font_name="Calibri"):
    txb = slide.shapes.add_textbox(Inches(x), Inches(y), Inches(w), Inches(h))
    tf = txb.text_frame
    tf.word_wrap = True
    p = tf.paragraphs[0]
    p.alignment = align
    run = p.add_run()
    run.text = text
    run.font.size = Pt(font_size)
    run.font.bold = bold
    run.font.italic = italic
    run.font.color.rgb = color
    run.font.name = font_name
    return txb

def dark_bg(slide):
    bg = slide.background.fill; bg.solid(); bg.fore_color.rgb = C_BG_DARK

def light_bg(slide):
    bg = slide.background.fill; bg.solid(); bg.fore_color.rgb = C_BG_LIGHT

def add_slide_number(slide, num):
    add_textbox(slide, 12.5, 7.1, 0.7, 0.3, str(num), 9, color=C_MUTED, align=PP_ALIGN.RIGHT)

def build_title_slide(prs, data):
    slide = prs.slides.add_slide(prs.slide_layouts[6]); dark_bg(slide)
    add_rect(slide, 9.5, -0.5, 5, 5, C_ACCENT1)
    add_rect(slide, 10.2, -0.3, 5, 5, C_BG_DARK)
    add_rect(slide, 0, 6.9, 13.33, 0.6, C_ACCENT1)
    add_rect(slide, 0, 0, 0.22, 7.5, C_ACCENT2)
    add_textbox(slide, 0.6, 1.8, 10, 1.6, data["title"], 52, bold=True, color=C_WHITE)
    if data.get("explanation"):
        add_textbox(slide, 0.6, 3.6, 9, 1.0, data["explanation"], 18, color=rgb(0xBB,0xBB,0xDD))
    add_textbox(slide, 0.6, 7.0, 8, 0.4, "Generated by PPTFinders AI", 11, color=C_WHITE)

def build_two_column_slide(prs, data, slide_num):
    slide = prs.slides.add_slide(prs.slide_layouts[6]); light_bg(slide)
    add_rect(slide, 0, 0, 5.5, 7.5, C_BG_MID)
    add_rect(slide, 0, 0, 5.5, 0.18, C_ACCENT1)
    add_textbox(slide, 0.35, 0.45, 4.8, 1.4, data["title"], 26, bold=True, color=C_WHITE)
    if data.get("explanation"):
        add_textbox(slide, 0.35, 2.0, 4.7, 4.8, data["explanation"], 13.5, color=rgb(0xCC,0xCC,0xEE))
    bullets = data.get("bullets", []); card_y = 0.5
    for i, bullet in enumerate(bullets[:5]):
        cc = [C_ACCENT1,C_ACCENT2,rgb(0x28,0x28,0x50),rgb(0x00,0x90,0xB0),rgb(0x55,0x20,0xCC)][i%5]
        add_rect(slide, 5.8, card_y, 7.1, 1.1, rgb(0xFF,0xFF,0xFF))
        add_rect(slide, 5.8, card_y, 0.12, 1.1, cc)
        add_rect(slide, 6.05, card_y+0.22, 0.6, 0.6, cc)
        add_textbox(slide, 6.05, card_y+0.22, 0.6, 0.6, str(i+1), 14, bold=True, color=C_WHITE, align=PP_ALIGN.CENTER)
        add_textbox(slide, 6.85, card_y+0.12, 5.9, 0.88, bullet, 13, color=C_TEXT_DARK)
        card_y += 1.22
    add_slide_number(slide, slide_num)

def build_fulltext_slide(prs, data, slide_num):
    slide = prs.slides.add_slide(prs.slide_layouts[6]); dark_bg(slide)
    add_rect(slide, 0, 0, 13.33, 0.14, C_ACCENT2)
    add_rect(slide, 11.8, 0.14, 1.53, 7.36, C_PANEL)
    add_textbox(slide, 0.5, 0.3, 11.0, 0.9, data["title"], 30, bold=True, color=C_WHITE)
    if data.get("explanation"):
        add_textbox(slide, 0.5, 1.4, 10.9, 2.6, data["explanation"], 15, color=rgb(0xCC,0xCC,0xEE))
    bullets = data.get("bullets", [])
    if bullets:
        add_rect(slide, 0, 4.3, 13.33, 0.04, C_ACCENT1)
        add_textbox(slide, 0.5, 4.45, 4, 0.35, "KEY POINTS", 9, bold=True, color=C_ACCENT2)
        col_x = [0.5, 4.0, 7.5]
        for i, bullet in enumerate(bullets[:3]):
            cx = col_x[i%3]
            add_rect(slide, cx, 4.95, 3.2, 2.2, C_PANEL)
            add_rect(slide, cx, 4.95, 3.2, 0.1, C_ACCENT1)
            add_textbox(slide, cx+0.15, 5.15, 2.9, 1.95, bullet, 12.5, color=C_WHITE)
