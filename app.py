def generate_website_code(user_prompt: str) -> str:
    client = Groq(api_key=API_KEY)

    system_prompt = """You are an elite full-stack web developer and UI/UX designer.
When given a website description, generate a COMPLETE, beautiful, single-file HTML website.

STRICT RULES — follow exactly:
1. Return ONLY raw HTML code starting with <!DOCTYPE html>. No explanation, no markdown, no backticks.
2. All CSS must be inside a <style> tag in <head>
3. All JavaScript must be inside a <script> tag before </body>
4. Must be a fully working standalone file — no external dependencies except Google Fonts CDN
5. Must include ALL of these sections:
   - Sticky navigation bar with logo/brand name and smooth scroll links
   - Hero section: big headline, subheadline, 1-2 CTA buttons, background visual
   - Features/Services section: 3-6 cards in a grid with icons (use emoji or CSS shapes)
   - About or How It Works section
   - Stats/Numbers section with impressive figures relevant to the topic
   - Testimonials section with 3 fake but realistic reviews
   - Contact form section (name, email, message fields — no backend needed)
   - Footer with links and copyright

DESIGN RULES:
- Pick a bold color palette that fits the topic (NOT generic blue/white)
- Use Google Fonts: link ONE display font + ONE body font
- Add CSS animations: fade-in on load, hover effects on cards and buttons
- Cards must have box shadows and smooth hover transform
- Smooth scroll behavior on navigation links
- Fully responsive with CSS Grid/Flexbox — works on mobile
- Write REAL content relevant to the prompt, not Lorem ipsum
- Include a mobile hamburger menu that toggles on click
- Make it look like a $5000 professionally designed website"""

    response = client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": f"Build a complete professional website for: {user_prompt}"}
        ],
        temperature=0.7,
        max_tokens=8000,
    )

    html = response.choices[0].message.content.strip()

    # Strip markdown fences if model added them
    if "```" in html:
        parts = html.split("```")
        for part in parts:
            p = part.strip()
            if p.startswith("html"): p = p[4:].strip()
            if p.lower().startswith("<!doctype") or p.startswith("<html"):
                html = p
                break

    # Ensure it starts with doctype
    if not html.lower().startswith("<!doctype") and "<html" not in html[:50]:
        html = "<!DOCTYPE html>\n<html lang='en'>\n" + html + "\n</html>"

    return html


