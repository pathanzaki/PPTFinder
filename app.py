def build_timeline_slide(prs, data, slide_num):
    slide = prs.slides.add_slide(prs.slide_layouts[6]); dark_bg(slide)
    add_rect(slide, 0, 0, 13.33, 0.14, C_ACCENT1)
    add_textbox(slide, 0.5, 0.3, 12, 0.9, data["title"], 30, bold=True, color=C_WHITE)
    if data.get("explanation"):
        add_textbox(slide, 0.5, 1.3, 12.3, 1.1, data["explanation"], 14, color=rgb(0xBB,0xBB,0xDD))
    add_rect(slide, 0.5, 3.1, 12.3, 0.08, C_ACCENT2)
    bullets = data.get("bullets", [])
    step_w = 12.3 / max(len(bullets), 1)
    for i, bullet in enumerate(bullets[:5]):
        cx = 0.5 + i*step_w + step_w/2 - 0.3
        add_rect(slide, cx, 2.82, 0.6, 0.6, C_ACCENT1)
        add_textbox(slide, cx, 2.82, 0.6, 0.6, str(i+1), 14, bold=True, color=C_WHITE, align=PP_ALIGN.CENTER)
        add_rect(slide, cx-0.5, 3.4, 1.6, 2.8, C_PANEL)
        add_textbox(slide, cx-0.4, 3.5, 1.5, 2.7, bullet, 11.5, color=C_WHITE)
    add_slide_number(slide, slide_num)

def build_grid_slide(prs, data, slide_num):
    slide = prs.slides.add_slide(prs.slide_layouts[6]); light_bg(slide)
    add_rect(slide, 0, 0, 13.33, 1.25, C_BG_MID)
    add_rect(slide, 0, 0, 0.22, 7.5, C_ACCENT2)
    add_textbox(slide, 0.5, 0.2, 12, 0.85, data["title"], 28, bold=True, color=C_WHITE)
    bullets = data.get("bullets", [])
    positions = [(0.4,1.45),(4.55,1.45),(8.7,1.45),(0.4,4.0),(4.55,4.0),(8.7,4.0)]
    accents = [C_ACCENT1,C_ACCENT2,C_ACCENT3,rgb(0x28,0x9B,0x6C),rgb(0xE0,0x60,0x20),rgb(0x44,0x44,0xCC)]
    for i, bullet in enumerate(bullets[:6]):
        if i >= len(positions): break
        px, py = positions[i]; ac = accents[i%len(accents)]
        add_rect(slide, px+0.05, py+0.05, 3.8, 2.3, rgb(0xDD,0xDD,0xEE))
        add_rect(slide, px, py, 3.8, 2.3, C_WHITE)
        add_rect(slide, px, py, 3.8, 0.22, ac)
        add_rect(slide, px+0.12, py+0.35, 0.55, 0.55, ac)
        add_textbox(slide, px+0.12, py+0.35, 0.55, 0.55, str(i+1), 14, bold=True, color=C_WHITE, align=PP_ALIGN.CENTER)
        add_textbox(slide, px+0.8, py+0.3, 2.85, 1.85, bullet, 12, color=C_TEXT_DARK)
    add_slide_number(slide, slide_num)

def build_conclusion_slide(prs, data):
    slide = prs.slides.add_slide(prs.slide_layouts[6]); dark_bg(slide)
    add_rect(slide, 0, 0, 13.33, 0.14, C_ACCENT2)
    add_rect(slide, 0, 7.36, 13.33, 0.14, C_ACCENT1)
    add_rect(slide, 0, 0, 0.22, 7.5, C_ACCENT1)
    add_textbox(slide, 1.5, 1.6, 10.3, 1.4, data["title"], 46, bold=True, color=C_WHITE, align=PP_ALIGN.CENTER)
    if data.get("explanation"):
        add_textbox(slide, 1.5, 3.2, 10.3, 1.5, data["explanation"], 16, color=rgb(0xBB,0xBB,0xDD), align=PP_ALIGN.CENTER)
    bullets = data.get("bullets", [])
    if bullets:
        bx = 2.5
        for b in bullets[:3]:
            add_textbox(slide, bx, 5.1, 3.0, 0.55, f"✦  {b}", 13, color=C_ACCENT2, align=PP_ALIGN.CENTER)
            bx += 3.0
    add_textbox(slide, 0.5, 6.95, 8, 0.45, "Generated by PPTFinders AI", 11, color=C_MUTED)

LAYOUT_CYCLE = [
    "two_column","fulltext","stat_callout","two_column","timeline",
    "grid","fulltext","stat_callout","two_column","grid","timeline",
    "fulltext","stat_callout","grid","two_column","timeline","fulltext",
    "grid","stat_callout","two_column",
]

def build_pptx(slides_data):
    prs = Presentation()
    prs.slide_width = SLIDE_W; prs.slide_height = SLIDE_H
    slide_num = 1
    for i, sd in enumerate(slides_data):
        stype = sd.get("slide_type", "content")
        if stype == "title": build_title_slide(prs, sd)
        elif stype == "conclusion": build_conclusion_slide(prs, sd)
        else:
            layout = LAYOUT_CYCLE[(i-1) % len(LAYOUT_CYCLE)]
            if layout == "two_column": build_two_column_slide(prs, sd, slide_num)
            elif layout == "fulltext": build_fulltext_slide(prs, sd, slide_num)
            elif layout == "stat_callout": build_stat_callout_slide(prs, sd, slide_num)
            elif layout == "timeline": build_timeline_slide(prs, sd, slide_num)
            elif layout == "grid": build_grid_slide(prs, sd, slide_num)
            slide_num += 1
    buf = io.BytesIO(); prs.save(buf); buf.seek(0)
    return buf.read()

def generate_slide_content(user_prompt, num_slides):
    client = Groq(api_key=API_KEY)
    num_slides = max(5, min(30, num_slides))
    content_slides = num_slides - 2
    system_prompt = f"""You are an expert presentation designer and content writer.
Return ONLY a valid JSON array of exactly {num_slides} slides. No markdown, no backticks, no extra text.
Each slide: "title" (max 8 words), "slide_type" (title|content|conclusion),
"explanation" (3-5 professional sentences), "bullets" (4-6 short phrases).
Slide 1=title, Slide {num_slides}=conclusion, rest=content ({content_slides} content slides, each different aspect).
Return ONLY raw JSON array."""
    response = client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[{"role":"system","content":system_prompt},
                  {"role":"user","content":f"Create a {num_slides}-slide presentation about: {user_prompt}"}],
        temperature=0.65, max_tokens=8000,
    )
    raw = response.choices[0].message.content.strip()
    if "```" in raw:
        raw = raw.split("```")[1]
        if raw.startswith("json"): raw = raw[4:]
    raw = raw.strip()
    slides = json.loads(raw)
    if slides: slides[0]["slide_type"]="title"; slides[-1]["slide_type"]="conclusion"
    return slides


