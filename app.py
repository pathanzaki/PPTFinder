@app.route("/")
def index():
    return render_template("index.html")

@app.route("/generate", methods=["POST"])
def generate_ppt():
    data = request.get_json()
    prompt = data.get("prompt", "").strip()
    num_slides = int(data.get("num_slides", 12))
    if not prompt:
        return jsonify({"error": "Please enter a topic."}), 400
    if num_slides < 5 or num_slides > 30:
        return jsonify({"error": "Slide count must be between 5 and 30."}), 400
    try:
        slides_data = generate_slide_content(prompt, num_slides)
        pptx_bytes = build_pptx(slides_data)
        filename = prompt[:35].replace(" ", "_") + ".pptx"
        return send_file(io.BytesIO(pptx_bytes),
            mimetype="application/vnd.openxmlformats-officedocument.presentationml.presentation",
            as_attachment=True, download_name=filename)
    except json.JSONDecodeError:
        return jsonify({"error": "AI returned bad data. Try again."}), 500
    except Exception as e:
        return jsonify({"error": str(e)}), 500

@app.route("/generate-website", methods=["POST"])
def generate_website():
    data = request.get_json()
    prompt = data.get("prompt", "").strip()
    if not prompt:
        return jsonify({"error": "Please enter a website description."}), 400
    try:
        html_code = generate_website_code(prompt)
        site_id = str(uuid.uuid4())[:8]
        filename = f"site_{site_id}.html"
        filepath = os.path.join(SITES_FOLDER, filename)
        with open(filepath, "w", encoding="utf-8") as f:
            f.write(html_code)
        return jsonify({
            "success": True,
            "preview_url": f"/preview/{filename}",
            "download_url": f"/download-site/{filename}",
            "filename": filename
        })
    except Exception as e:
        return jsonify({"error": str(e)}), 500

@app.route("/preview/<filename>")
def preview_site(filename):
    return send_from_directory(SITES_FOLDER, filename)

@app.route("/download-site/<filename>")
def download_site(filename):
    return send_from_directory(SITES_FOLDER, filename,
        as_attachment=True, download_name=filename.replace("site_","website_"))

